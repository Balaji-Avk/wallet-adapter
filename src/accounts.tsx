import { useConnection,useWallet } from "@solana/wallet-adapter-react";
import {TOKEN_PROGRAM_ID} from '@solana/spl-token'
import { useEffect, useState } from "react";
import { AccountInfo, LAMPORTS_PER_SOL, PublicKey } from '@solana/web3.js'; 
type ProgramAccount = {
    account: AccountInfo<Buffer>;
    pubkey: PublicKey;
  };

function Accounts(){
    const wallet = useWallet();
    const {connection} = useConnection();
    const [accounts,setAccounts] = useState<ProgramAccount[]>([]);

    async function getAccounts(){
        if(wallet.publicKey ){
            const account = await connection.getTokenAccountsByOwner(wallet.publicKey,{programId : TOKEN_PROGRAM_ID})
            const values = [...account.value]
            setAccounts(values);
            console.log(account.value);
        
        }
        else{
            alert("no accounts found")            
        }
    }
    useEffect(() => {
        (async () =>{
            if(wallet.connected){
                await getAccounts();
                 console.log("connecting")
            }
            else{
                console.log("walllet not connected")
            }
        })()
    }, [wallet.connected]);
    return(
        <div className=''>
            {
                accounts.map((ele)=>{
                    return(
                        <div>
                            <div>public key : {ele.pubkey.toString()}</div>
                            <div>balance : {ele.account.lamports/LAMPORTS_PER_SOL}</div>
                        </div>
                    )
                })
            }    
            
        </div>
    )
}

export default Accounts;

